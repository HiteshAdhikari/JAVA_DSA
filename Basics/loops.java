package Basics;
 class loops {
    public static void main(String[] args) {

        // for loop
        for(int  i = 0 ; i<3; i++){
            System.out.println("Hey");
        }
        // while loop
        int j = 10;
        while( j != 0 ){
            System.out.println(j);
            j--;
        }
        

    }
    
}
